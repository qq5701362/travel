package jx.tour.pojo;

public class ScenicComment extends Sccomment {
	 private String username;
	 private String userimag;
	 private int scenicNum;
	 private String name;//景点名称
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserimag() {
		return userimag;
	}
	public void setUserimag(String userimag) {
		this.userimag = userimag;
	}
	public int getScenicNum() {
		return scenicNum;
	}
	public void setScenicNum(int scenicNum) {
		this.scenicNum = scenicNum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
